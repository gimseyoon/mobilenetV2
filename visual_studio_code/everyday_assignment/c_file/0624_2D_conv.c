#include <stdio.h>

#define X_ROW 10
#define X_COLUMN 10
#define W_ROW 3
#define W_COLUMN 3




int main()
{
    ///////////////////////////////////////////////////
    //declare//////////////////////////////////////////
    /////////////////////////////////////////////////// 
    int x[10][10] = {{4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5},
                     {4,5,6,1,2,3,4,1,3,5}};
    
    int w[3][3] = {{1,0,-1},{1,0,-1},{1,0,-1}};
    int y[8][8] = {0, };





    ///////////////////////////////////////////////////
    //print x//////////////////////////////////////////
    ///////////////////////////////////////////////////   
    
    for(int i=0; i < ( X_ROW); i++) // 0~11
    {
        for(int j = 0; j < ( X_COLUMN); j++) // 0~11
        {
            printf("x[%d][%d] = %d  ", i,j,x[i][j]);

            if(j==(X_ROW - 1)) printf("\n");
        }
    }
    

    printf("\n");





    ///////////////////////////////////////////////////
    // compute part ///////////////////////////////////
    ///////////////////////////////////////////////////

    for(int i=0; i < ( X_ROW - W_ROW + 1); i++) // 0~7
    {
        for(int j = 0; j < ( X_COLUMN - W_COLUMN + 1); j++) // 0~7
        {
            for(int k = 0; k < W_ROW; k++) // 0~2 -> weight, three row compute
            {
                for(int p = 0; p < W_COLUMN; p++) // 0~2 -> weight, one row compute 
                {
                    y[i][j] += (x[i+k][j+p]) * (w[k][p]);
                    
                    printf("i=%d, j=%d, k=%d, p=%d\t",i,j,k,p);
                    printf("y[%d][%d] = %d\n", i,j,y[i][j]);
                    
                  
                }     
            }  
        }
    }
    printf("\n");





    

    ///////////////////////////////////////////////////
    //print result ////////////////////////////////////
    ///////////////////////////////////////////////////
    
    for(int i=0; i < ( X_ROW - W_ROW + 1); i++) // 0~7
    {
        for(int j = 0; j < ( X_ROW - W_ROW + 1); j++) // 0~7
        {
            printf("y[%d][%d] = %d\t", i,j,y[i][j]);

            if(j==7) printf("\n");
        }
    }





    return 0;
} 
